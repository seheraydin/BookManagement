    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String name = txtBookName.getText();
        String writer = txtBookWriter.getText();
        String type = txtBookType.getText();
        String publisher = txtBookPublisher.getText();
        
        int selectedRow =book_table.getSelectedRow();
        
        //eğer tablodan hiçbir şey seçilmemişse ya da boş bir alana tıklanmış olabilir
        //tablodaki değer yoksa 
        if (selectedRow == -1) {
            //tabloda bir şey seçmeden edit yapmak isterse
            if (model.getRowCount()== 0) {
                lblMessage2.setText("Book table null");
                
            }
            else{
              lblMessage2.setText("Book select pls");  
            }
                
            
        }
        else{
            int id =(int) model.getValueAt(selectedRow , 0);
            op.bookUpdate(id, name, writer, type, publisher);
            bookView();
            lblMessage.setText("Book update succesfull");
            
        }
        
    }//GEN-LAST:event_btnEditActionPerformed

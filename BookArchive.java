    public void filter(String query){
        //tableRowSorter kullanarak DefaultTableModel yapısından oluşturarak bir trs referansı aldık
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<DefaultTableModel>(model);
        book_table.setRowSorter(trs);//kendi tablomuzdan satır satır arama için
        trs.setRowFilter(RowFilter.regexFilter(query));//kullanıcıdan aldığımız sorguyu regexFilter ile aldık
        
    }
